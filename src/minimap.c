int draw_minimap(t_data *data)
{
    int rows = data->map.rows;
    int columns = data->map.columns;
    rows = 24;
    columns = 24;
    int i;
    int j;
    int color;
    i = 0;
    while (i < rows)
    {
        j = 0;
        while (j < columns)
        {
            if (againSameMap[i][j] == false)
            {
                color = 0x0000CC;
            }
            else
			{
			 	color = 0xFF9999;
			}
			draw_16px(&data->img, i, j, color);
            j++;
        }
		i++;
    }
	draw_player(data);
	if (data == NULL)
	{
		return 1;
	}
    return (0);
}