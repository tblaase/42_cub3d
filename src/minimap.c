int	draw_25px(t_img *img, int x, int y, int color) //think about turning it into void
{
	int	i;
	int	j;

	i = 0;
	while (i < 5)
	{
		j = 0;
		while (j < 5)
		{
			my_mlx_pixel_put(img, (x * 5) + i, (y * 5) + j, color);
			j++;
		}
		i++;
	}
	return (EXIT_SUCCESS);
}

int	draw_player(t_data *data) //think about turning it into void
{
	int	i;
	int	j;

	i = 0;
	while (i < 3)
	{
		j = -1;
		while (j < 3)
		{
			my_mlx_pixel_put(&data->img, (data->p_y * 5) + i, (data->p_x * 5) + j, 0xDC143C);
			j++;
		}
		i++;
	}
	return (EXIT_SUCCESS);
}

int	draw_minimap(t_data *data) //think about turning it into void
{
	int	rows = data->map.rows;
	int	columns = data->map.columns;
	int	i;
	int	j;
	int	color;

	rows = 24;
	columns = 24;
	i = 0;
	while (i < columns)
	{
		j = 0;
		while (j < rows)
		{
			if (againSameMap[j][i] == false)
				color = 0xf0f8ff;
			else
				color = 0x1E90FF;
			draw_25px(&data->img, i, j, color);
			j++;
		}
		i++;
	}
	draw_player(data);
	return (EXIT_SUCCESS);
}