int draw_16px(t_img *img, int x, int y, int color)
{
	int i;
	int j;
	i = 0;
	while (i < 5)
	{
		j = 0;
		while (j < 5)
		{
			my_mlx_pixel_put(img, (x * 5) + i, (y * 5)+j, color);
			j++;
		}
		i++;
	}
	return (0);
}

