int draw_25px(t_img *img, int x, int y, int color)
{
	int i;
	int j;
	i = 0;
	while (i < 5)
	{
		j = 0;
		while (j < 5)
		{
			my_mlx_pixel_put(img, (x * 5) + i, (y * 5) + j, color);
			j++;
		}
		i++;
	}
	return (0);
}

int draw_player(t_data *data)
{
	int i;
	int j;
	i = 0;
	while (i < 3)
	{
		j = -1;
		while (j < 3)
		{
			my_mlx_pixel_put(&data->img, (data->p_y * 5) + i, (data->p_x * 5) + j, 0xDC143C);
			j++;
		}
		i++;
	}
	return (0);
}

int draw_minimap(t_data *data)
{
	int rows;
	int columns;
	int i;
	int j;
	int color;

	rows = data->map.rows;
	columns = data->map.columns;
	i = 0;
	while (i < rows)
	{
		j = 0;
		while (j < columns)
		{
			color = get_color(data->map.map[j][i]);
			if (color)
				draw_25px(&data->img, i, j, color);
			j++;
		}
		i++;
	}
	draw_player(data);
	return (0);
}